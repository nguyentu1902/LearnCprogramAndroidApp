package com.example.testfirebase.Object;

public class AdvanceCourse {
    private int imgAD;
    private String nameAD;

    public AdvanceCourse() {
    }

    public AdvanceCourse(int imgAD, String nameAD) {
        this.imgAD = imgAD;
        this.nameAD = nameAD;
    }

    public int getImgAD() {
        return imgAD;
    }

    public void setImgAD(int imgAD) {
        this.imgAD = imgAD;
    }

    public String getNameAD() {
        return nameAD;
    }

    public void setNameAD(String nameAD) {
        this.nameAD = nameAD;
    }
}
